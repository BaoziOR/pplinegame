//定义一个“类”  
function rankS() {
  this.id = 0;
  this.url = "";
  this.nickname= "";
  this.score =0;
  this.lvs = 0;
  this.minute=0;
  this.second = 0;
  this.step = -1;
}

//定义设置变量方法：setConflictData
rankS.prototype.setRankData = function (id, url, nickname, score, lvs, minute, second,step) {
  this.id =id;
  this.url = url;
  this.nickname = nickname;
  this.score = score;
  this.lvs = lvs;
  this.minute = minute;
  this.second = second;
  this.step = step;
}

module.exports = {
  rankS: rankS
}